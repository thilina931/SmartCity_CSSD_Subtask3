     public void clear() {
        firstName.setText("");
        lastName.setText("");
        city.setText("");
        email.setText("");
        mobile.setText("");
        password.setText("");
        password1.setText("");

        theUsers.removeAll(theUsers);
        try {
            for (User user : Serialization.deserializeUsers()) {
                theUsers.addUser(user);
                user.print();
            }
        } catch (IOException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
     
     
     
     
     
